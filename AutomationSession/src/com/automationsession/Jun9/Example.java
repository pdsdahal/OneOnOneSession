package com.automationsession.Jun9;

public class Example {

    public static void main(String[] args) {

        for(int i=0;i<5;i++){// i = 0, 1

            for(int j=0;j<=i;j++){// 0 <= 0
                System.out.print(" * ");
            }

            System.out.println("\n");
        }

    }
}
