package com.automationsession.May29;

public class UniaryOperator {
    public static void main(String[] args) {

        int num1 = 10;

        //uniary minus
        int num2 = -num1;
        System.out.println("Uniary minus : "+num2);

        //postfix increment
        num1++;//num1 + 1 : num1 = 11
        System.out.println("num1 +1  = "+num1);

        //deceremnt
        num1--;// num1-1 = 11-1 = 10
        System.out.println("num1 - 1  = "+num1);

    }
}
