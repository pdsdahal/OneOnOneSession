package com.automationsession.May28;

public class AssignmentOperator {

    public static void main(String[] args) {

        // simple assignment operator
        String name = "Sangita";
        int age = 13;
        String address = "Ktm";
        boolean onlineStatus = true;


        int num1  = 2;
        int num2 = 3;

        //+=  Add and assignment operator
        num2 += num1; // num2  = num2 + num1 // 3 + 2 = 5
        System.out.println("Sum 2 and 3  = "+num2);



        int num3 = 10;
        int num4  = 20;

        //substract and assignment operator
        num4 -= num3; //num4 = num4 - num3

        System.out.println("20 - 10 : "+num4);

    }
}
