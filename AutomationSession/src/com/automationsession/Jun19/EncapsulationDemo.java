package com.automationsession.Jun19;

public class EncapsulationDemo {

    private int len;
    private int bre;


    public int getLen() {
        return len;
    }

    public void setLen(int len) {
        this.len = len;
    }

    public int getBre() {
        return bre;
    }

    public void setBre(int bre) {
        this.bre = bre;
    }
}